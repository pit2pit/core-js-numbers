/**
 * Returns the sum of the digits of a given number.
 *
 * @param {number} num
 * @return {number}
 *
 * @example:
 *   123 => 6  // (1+2+3)
 *   202 => 4  // (2+0+2)
 *   5   => 5  // 5
 */
function getSumOfDigits(num) {
  return ((num - 1) % 9) + 1;
}

function isPowerOfTwo(num) {
  if (num > 0 && Math.log2(num) % 1 === 0) {
    return true;
  }
  return false;
}

